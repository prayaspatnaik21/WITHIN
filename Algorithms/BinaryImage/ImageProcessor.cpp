////////////////////////////////////////////////////////////////////////////////////

#include "ImageProcessor.h"

////////////////////////////////////////////////////////////////////////////////////

void ImageProcessor :: setAlgorithm(std::unique_ptr<BinaryImageFactory> newAlgo)
{
    algo = std::move(newAlgo);
}

////////////////////////////////////////////////////////////////////////////////////

cv::Mat ImageProcessor :: run(cv::Mat& imageData)
{
    if(algo)
        return algo->process(imageData);
    return {};
}

