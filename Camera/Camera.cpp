///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include "Camera.h"

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

Camera :: Camera(std::unique_ptr<ThreadSafeQueue<cv::Mat>> buffer)
:buffer(std::move(buffer))
,running(true)
{
    std::cout << "Camera Ctor caled" << std::endl;
    if(initCamera())
        imageCaptureThread = std::thread(&Camera::captureFrames , this);
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

Camera :: ~Camera()
{
    running.store(false);

    if (cap.isOpened())
        cap.release();

    if (imageCaptureThread.joinable())
        imageCaptureThread.join();
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

bool Camera :: initCamera()
{
    std::string pipeline =
        "v4l2src device=/dev/video0 ! "
        "image/jpeg, width=640, height=480, framerate=30/1 ! "
        "jpegdec ! videoconvert ! "
        "appsink drop=true sync=false";

    cap.open(pipeline, cv::CAP_GSTREAMER);

    if (!cap.isOpened())
    {
        std::cerr << "ERROR: Camera open failed\n";
        return false;
    }
    else
    {
        std::cout << "Camera Open Successful" << std::endl;
    }
    return true;
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Camera :: captureFrames()
{
    cv::Mat temp;

    while (running.load())
    {
        cap.read(temp);
        if (temp.empty())
            continue;
        buffer->push(temp);
    }
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

cv::Mat Camera :: getFrame()
{
    auto frame = buffer->pop();
    return frame;
}

