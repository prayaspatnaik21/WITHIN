///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include "CameraCapture.h"

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

CameraCapture :: Camera(ThreadSafeQueue<cv::Mat>& buffer)
:buffer(buffer)
,running(true)
{
    imageCaptureThread = std::thread(&Camera::captureFrames , this);
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

Camera :: ~Camera()
{
    running.store(false);

    if (cap.isOpened())
        cap.release();

    if (imageCaptureThread.joinable())
        imageCaptureThread.join();
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

bool CameraCapture :: initCamera()
{
    std::string pipeline =
        "v4l2src device=/dev/video0 ! "
        "image/jpeg, width=640, height=480, framerate=30/1 ! "
        "jpegdec ! videoconvert ! "
        "appsink drop=true sync=false";

    cap.open(pipeline, cv::CAP_GSTREAMER);

    if (!cap.isOpened())
    {
        std::cerr << "ERROR: Camera open failed\n";
        return false;
    }
    return true;
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void CameraCapture :: captureFrames()
{
    cv::Mat temp;

    while (running)
    {
        cap.read(temp);
        if (temp.empty())
            continue;

        buffer.push(temp);
    }
}
